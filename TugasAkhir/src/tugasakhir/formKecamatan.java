    private void baca_data(){
        try{
            stm  = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsKc = stm.executeQuery("select * from tblkecamatan");
            ResultSetMetaData meta =  RsKc.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while ( RsKc.next()) {
                baris =  RsKc.getRow();
            }
            dataTable = new Object [baris] [col];
            int x = 0;
            RsKc.beforeFirst();
            while(RsKc.next()){
                dataTable[x][0] = RsKc.getString("kd_kecamatan");
                dataTable[x][1] = RsKc.getString("nm_kecamatan");
                x++;
            }
            tblKc.setModel(new DefaultTableModel(dataTable, header));
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
