    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String tKode = txtKdKc.getText();
        String tNama = txtNmKc.getText();
        try{
            if(edit == true){
                stm.executeUpdate("update tblkecamatan set nm_kecamatan='" + tNama + "'where kd_kecamatan='" + tKode + "'");
            }else{
                stm.executeUpdate("INSERT into tblkecamatan VALUES('" + tKode + "','" + tNama + "')");
            }
            tblKc.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
            kosong();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

