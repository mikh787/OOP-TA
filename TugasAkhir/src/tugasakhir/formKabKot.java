    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String tKode = txtKdKab.getText();
        String tNama = txtNmKab.getText();
        try{
            if(edit == true){
                stm.executeUpdate("update tblkabkot set nm_kabkot='" + tNama + "'where kd_kabkot='" + tKode + "'");
            }else{
                stm.executeUpdate("INSERT into tblkabkot VALUES('" + tKode + "','" + tNama + "')");
            }
            tblKab.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
            kosong();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

