    private void baca_data(){
        try{
            stm  = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsKab = stm.executeQuery("select * from tblkabkot");
            ResultSetMetaData meta =  RsKab.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while ( RsKab.next()) {
                baris =  RsKab.getRow();
            }
            dataTable = new Object [baris] [col];
            int x = 0;
            RsKab.beforeFirst();
            while(RsKab.next()){
                dataTable[x][0] = RsKab.getString("kd_kabkot");
                dataTable[x][1] = RsKab.getString("nm_kabkot");
                x++;
            }
            tblKab.setModel(new DefaultTableModel(dataTable, header));
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
