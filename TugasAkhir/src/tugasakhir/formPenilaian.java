    private void baca_data(){
        try{
            stm  = Con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsPs = stm.executeQuery("select * from tblnilai");
            ResultSetMetaData meta = RsPs.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while (RsPs.next()) {
                baris = RsPs.getRow();
            }
            dataTable = new Object [baris] [col];
            int x = 0;
            RsPs.beforeFirst();
            while(RsPs.next()){
                dataTable[x][0] = RsPs.getString("kd_peserta");
                dataTable[x][1] = RsPs.getString("nm_peserta");
                dataTable[x][2] = RsPs.getString("ipk");
                dataTable[x][3] = RsPs.getString("nilai_tulis");
                dataTable[x][4] = RsPs.getString("nilai_wawancara");
                dataTable[x][5] = RsPs.getString("nilai_akhr");
                x++;
            }
            tblPeserta.setModel(new DefaultTableModel(dataTable, header));
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
