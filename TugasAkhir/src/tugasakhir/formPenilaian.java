    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String tKode = txtKd.getText();
        String tNama = txtNm.getText();
        String tIpk = txtIPK.getText();
        String tNilaiT = txtNilaiT.getText();
        String tNilaiWw = txtNilaiW.getText();
        hitungNilai();
        String tNA = txtNa.getText();
        try {
            if(edit == true){
                stm.executeUpdate("update tblnilai set nm_peserta='" + tNama + "',ipk='" + tIpk + "',nilai_tulis=='" + tNilaiT + "',nilai_wawancara='" 
                        + tNilaiWw + "',nilai_akhr='" + tNA +"'where kd_peserta='" + tKode + "'");
            }else{
                stm.executeUpdate("INSERT into tblnilai VALUES('" + tKode + "','" + tNama + "','" + tIpk + "','" + tNilaiT + "','" + 
                        tNilaiWw + "','" + tNA + "')");
            }
            tblPeserta.setModel(new DefaultTableModel(dataTable, header));
            baca_data();
            aktif(false);
            setTombol(true);
            kosong();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
            System.out.println("Error : "+e);
        }        
    }//GEN-LAST:event_btnSimpanActionPerformed

